export function sum(firstOperand, secondOperand) {
  return firstOperand + secondOperand;
}

export function substract(firstOperand, secondOperand) {
  return firstOperand - secondOperand;
}

export function multiply(firstOperand, secondOperand) {
  return firstOperand * secondOperand;
}

export function divide(firstOperand, secondOperand) {
  return firstOperand / secondOperand;
}

export function inverseofNumber(number) {
  return (1 / parseFlaot(number)).toFixed(2);
}
